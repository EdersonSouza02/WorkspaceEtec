package br.com.sp.gov.gravadora;

public class GravadoraVO {

	private int codigoGravadora;
	private String nomeGravadora;
	private String enderecoGravadora;
	private String telefoneGravadora;
	private String contatoGravadora;
	private String urlGravadora;

	public int getCodigoGravadora() {
		return codigoGravadora;
	}

	public void setCodigoGravadora(int codigoGravadora) {
		this.codigoGravadora = codigoGravadora;
	}

	public String getNomeGravadora() {
		return nomeGravadora;
	}

	public void setNomeGravadora(String nomeGravadora) {
		this.nomeGravadora = nomeGravadora;
	}

	public String getEnderecoGravadora() {
		return enderecoGravadora;
	}

	public void setEnderecoGravadora(String enderecoGravadora) {
		this.enderecoGravadora = enderecoGravadora;
	}

	public String getTelefoneGravadora() {
		return telefoneGravadora;
	}

	public void setTelefoneGravadora(String telefoneGravadora) {
		this.telefoneGravadora = telefoneGravadora;
	}

	public String getContatoGravadora() {
		return contatoGravadora;
	}

	public void setContatoGravadora(String contatoGravadora) {
		this.contatoGravadora = contatoGravadora;
	}

	public String getUrlGravadora() {
		return urlGravadora;
	}

	public void setUrlGravadora(String urlGravadora) {
		this.urlGravadora = urlGravadora;
	}

}
